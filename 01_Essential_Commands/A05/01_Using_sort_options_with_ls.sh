#Change the current directory to '/etc'
cd /etc

#List the files in the current directory in long format, which includes detailed information such as file permissions, number of links, owner, group, size, and time of last modification
ls -l

#List the files in the current directory in long format, sorted by modification time, with the newest files at the end
ls -ltr

#List the files in the current directory in long format, sorted by modification time, with the newest files at the top
ls -lt

